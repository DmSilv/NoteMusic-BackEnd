// @desc    Obter pr√≥ximo m√≥dulo recomendado
// @route   GET /api/modules/next-recommended
// @access  Private
exports.getNextRecommended = async (req, res, next) => {
  try {
    const user = await User.findById(req.user.id);
    const completedModuleIds = user.completedModules.map(cm => cm.moduleId.toString());

    // Buscar m√≥dulos n√£o completados do n√≠vel do usu√°rio
    const nextModule = await Module.findOne({
      _id: { $nin: completedModuleIds },
      level: user.level,
      isActive: true
    })
    .sort({ order: 1 })
    .populate('prerequisites', 'title');

    if (!nextModule) {
      // Se completou todos do n√≠vel, sugerir pr√≥ximo n√≠vel
      const nextLevelMap = {
        'aprendiz': 'virtuoso',
        'virtuoso': 'maestro',
        'maestro': null
      };

      const nextLevel = nextLevelMap[user.level];
      
      if (nextLevel) {
        const nextLevelModule = await Module.findOne({
          level: nextLevel,
          isActive: true
        }).sort({ order: 1 });

        return res.json({
          success: true,
          message: `Parab√©ns! Voc√™ completou todos os m√≥dulos do n√≠vel ${user.level}. Que tal avan√ßar para o n√≠vel ${nextLevel}?`,
          suggestLevelUp: true,
          nextLevel,
          module: nextLevelModule
        });
      } else {
        return res.json({
          success: true,
          message: 'Parab√©ns! Voc√™ completou todos os m√≥dulos dispon√≠veis!',
          allCompleted: true
        });
      }
    }

    res.json({
      success: true,
      module: nextModule
    });
  } catch (error) {
    next(error);
  }
};

