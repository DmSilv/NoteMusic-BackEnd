const Module = require('../models/Module');
const User = require('../models/User');

// @desc    Listar todos os m√≥dulos
// @route   GET /api/modules
// @access  Public
exports.getModules = async (req, res, next) => {
  try {
    const { category, level, isActive = true } = req.query;
    
    // Construir filtro
    const filter = { isActive };
    if (category) filter.category = category;
    if (level) filter.level = level;

    const modules = await Module.find(filter)
      .sort({ order: 1 })
      .select('-content.exercises'); // N√£o enviar exerc√≠cios na listagem

    // Para endpoints p√∫blicos, n√£o verificar progresso do usu√°rio
    const modulesWithProgress = modules.map(module => ({
      ...module.toObject(),
      isCompleted: false,
      isLocked: false
    }));

    res.json({
      success: true,
      count: modulesWithProgress.length,
      modules: modulesWithProgress
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Obter m√≥dulo espec√≠fico
// @route   GET /api/modules/:id
// @access  Private
exports.getModule = async (req, res, next) => {
  try {
    const module = await Module.findById(req.params.id)
      .populate('prerequisites', 'title order')
      .populate('quizzes', 'title description questions.length');

    if (!module) {
      return res.status(404).json({
        success: false,
        message: 'M√≥dulo n√£o encontrado'
      });
    }

    // Verificar se usu√°rio completou os pr√©-requisitos
    const user = await User.findById(req.user.id);
    const completedModuleIds = user.completedModules.map(cm => cm.moduleId.toString());
    
    const hasPrerequisites = module.prerequisites.every(prereq => 
      completedModuleIds.includes(prereq._id.toString())
    );

    if (!hasPrerequisites && module.prerequisites.length > 0) {
      return res.status(403).json({
        success: false,
        message: 'Voc√™ precisa completar os m√≥dulos anteriores primeiro',
        prerequisites: module.prerequisites
      });
    }

    // Verificar se j√° foi completado
    const isCompleted = completedModuleIds.includes(module._id.toString());

    res.json({
      success: true,
      module: {
        ...module.toObject(),
        isCompleted,
        userCanAccess: hasPrerequisites || module.prerequisites.length === 0
      }
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Marcar m√≥dulo como completo
// @route   POST /api/modules/:id/complete
// @access  Private
exports.completeModule = async (req, res, next) => {
  try {
    const moduleId = req.params.id;
    const user = await User.findById(req.user.id);

    // Verificar se m√≥dulo existe
    const module = await Module.findById(moduleId);
    if (!module) {
      return res.status(404).json({
        success: false,
        message: 'M√≥dulo n√£o encontrado'
      });
    }

    // Verificar se j√° foi completado
    const alreadyCompleted = user.completedModules.some(
      cm => cm.moduleId.toString() === moduleId
    );

    if (alreadyCompleted) {
      return res.status(400).json({
        success: false,
        message: 'M√≥dulo j√° foi completado'
      });
    }

    // Adicionar m√≥dulo aos completados
    user.completedModules.push({
      moduleId,
      completedAt: new Date()
    });

    // Adicionar pontos
    user.totalPoints += module.points;

    // Atualizar progresso semanal
    user.weeklyProgress += 1;

    // Atualizar streak
    user.updateStreak();

    // Verificar progress√£o de n√≠vel
    const oldLevel = user.level;
    const newLevel = checkLevelProgression(user);
    let levelUpMessage = null;
    
    if (newLevel !== oldLevel) {
      user.level = newLevel;
      levelUpMessage = `üéâ Parab√©ns! Voc√™ avan√ßou para o n√≠vel ${getNivelName(newLevel)}!`;
    }

    await user.save();

    // Calcular pr√≥ximo n√≠vel e requisitos
    const levelInfo = getLevelInfo(user);

    res.json({
      success: true,
      message: 'M√≥dulo completado com sucesso!',
      pointsEarned: module.points,
      totalPoints: user.totalPoints,
      streak: user.streak,
      levelUp: levelUpMessage,
      currentLevel: getNivelName(user.level),
      levelInfo
    });
  } catch (error) {
    next(error);
  }
};

// Fun√ß√£o para verificar progress√£o de n√≠vel
const checkLevelProgression = (user) => {
  const completedModules = user.completedModules.length;
  const totalPoints = user.totalPoints;
  
  console.log(`üîç Verificando progress√£o de n√≠vel para usu√°rio:`, {
    level: user.level,
    completedModules,
    totalPoints
  });
  
  // Crit√©rios para progress√£o AJUSTADOS (mais justos):
  // Aprendiz -> Virtuoso: 2 m√≥dulos OU 150 pontos
  // Virtuoso -> Maestro: 4 m√≥dulos OU 300 pontos
  
  if (user.level === 'aprendiz') {
    if (completedModules >= 2 || totalPoints >= 150) {
      console.log(`‚úÖ Usu√°rio progrediu de Aprendiz para Virtuoso!`);
      return 'virtuoso';
    }
  } else if (user.level === 'virtuoso') {
    if (completedModules >= 4 || totalPoints >= 300) {
      console.log(`‚úÖ Usu√°rio progrediu de Virtuoso para Maestro!`);
      return 'maestro';
    }
  }
  
  return user.level; // Manter n√≠vel atual
};

// Fun√ß√£o para obter informa√ß√µes do n√≠vel
const getLevelInfo = (user) => {
  const completedModules = user.completedModules.length;
  const totalPoints = user.totalPoints;
  
  switch (user.level) {
    case 'aprendiz':
      return {
        currentLevel: 'Aprendiz',
        nextLevel: 'Virtuoso',
        progress: {
          modules: { current: completedModules, required: 2, percentage: Math.min((completedModules / 2) * 100, 100) },
          points: { current: totalPoints, required: 150, percentage: Math.min((totalPoints / 150) * 100, 100) }
        },
        requirements: 'Complete 2 m√≥dulos OU ganhe 150 pontos',
        benefits: 'Acesso a m√≥dulos intermedi√°rios e novos desafios'
      };
    
    case 'virtuoso':
      return {
        currentLevel: 'Virtuoso',
        nextLevel: 'Maestro',
        progress: {
          modules: { current: completedModules, required: 4, percentage: Math.min((completedModules / 4) * 100, 100) },
          points: { current: totalPoints, required: 300, percentage: Math.min((totalPoints / 300) * 100, 100) }
        },
        requirements: 'Complete 4 m√≥dulos OU ganhe 300 pontos',
        benefits: 'Acesso a todos os m√≥dulos e desafios especiais'
      };
    
    case 'maestro':
      return {
        currentLevel: 'Maestro',
        nextLevel: 'N√≠vel M√°ximo',
        progress: {
          modules: { current: completedModules, required: completedModules, percentage: 100 },
          points: { current: totalPoints, required: totalPoints, percentage: 100 }
        },
        requirements: 'Voc√™ atingiu o n√≠vel m√°ximo!',
        benefits: 'Acesso completo a todos os recursos'
      };
    
    default:
      return null;
  }
};

// Fun√ß√£o para obter nome do n√≠vel em portugu√™s
const getNivelName = (level) => {
  const names = {
    'aprendiz': 'Aprendiz',
    'virtuoso': 'Virtuoso', 
    'maestro': 'Maestro'
  };
  return names[level] || level;
};

